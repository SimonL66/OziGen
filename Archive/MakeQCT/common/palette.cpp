long SQUARE(long x)
{
	if (x<0)
		return x*x;
	else
		return x*x*7/8;
}

long RGBtoPalette(unsigned char * palette, long nColours, long r, long g, long b)
{
// No, so find the best match
long i, value, best=0, best_value=0x7fffffff;
	for (i=0; i<nColours; i++) {
		value = SQUARE(palette[i*4] - r) +
				SQUARE(palette[i*4+1] - g) +
				SQUARE(palette[i*4+2] - b);
		if (value <= best_value) {
			best_value = value;
			best = i;
		}
	}
	return best;
}

int RGB_to_tracklog_palette(int r, int g, int b)
{
	return RGBtoPalette(get_tracklog_palette(), 256, r, g, b);
}

